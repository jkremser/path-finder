    @Action
    public Task generate() {
        if (validate()) {
            return new GenerateTask(getApplication());
        } else {
              JOptionPane.showMessageDialog(ConverterApp.getApplication().getMainFrame(),"Zadejte korektní cesty k souborům.");
        }
        return null;
    }    

    private class GenerateTask extends org.jdesktop.application.Task<Object, Void> {

        GenerateTask(org.jdesktop.application.Application app) {
            // Runs on the EDT.  Copy GUI state that
            // doInBackground() depends on from parameters
            // to ChooseFileTask fields, here.
            super(app);
        }

        @Override
        protected Object doInBackground() {


            long start = System.currentTimeMillis();

            try {
                setProgress(0, 0, 26);
                setMessage("Načítání zastávek...");
                setProgress(1, 0, 25);
                StationsConverter stc = new StationsConverter(stationsField.getText(), outField.getText(),false);
                stc.load();
                setMessage("Načítání služeb...");
                setProgress(2, 0, 26);
                ServicesConverter sec = new ServicesConverter(servicesField.getText(), outField.getText(), stc);
                sec.convert();
                setMessage("Generování souboru se zastávkami...");
                setProgress(3, 0, 26);
                stc.setSec(sec);
                stc.generate();
                                
                JTextField[] daysFields = {sunday,monday,tuesday,wednesday,thursday,friday,saturday};
                int filter = filterSlider.getValue();
                int i = 4;
                int j = 0;
                for (Object day : DaysConverter.Days.values()) {
                    setMessage("Načítání souborů se službami - " + day);
                    setProgress(i++, 0, 26);
                    DaysConverter dac = new DaysConverter(daysFields[j++].getText(), outField.getText(), stc, sec, DaysConverter.Days.valueOf(day.toString()),false);
                    dac.load();
                    setMessage("Filtrování málo frekventovaných spojů (vozovny...)");
                    setProgress(i++, 0, 26);
                    dac.filter(filter);
                    dac.recognizeDirections();
                    setMessage("Generování souborů se službami - " + day);
                    setProgress(i++, 0, 26);
                    dac.generate();
                }
            } catch (IOException ex) {
                Logger.getLogger(ConverterView.class.getName()).log(Level.SEVERE, null, ex);
            }
            setMessage("Generování výsledného jar souboru");
            setProgress(26, 0, 26);
            Packager p = new Packager(archiveField.getText(),outField.getText());
            p.zip();
            p.createJadFile();
            setMessage("Hotovo. Celkový čas: " + (System.currentTimeMillis() - start) + " ms");
            return null;  // return your result
        }

        @Override
        protected void succeeded(Object result) {
        }
    }


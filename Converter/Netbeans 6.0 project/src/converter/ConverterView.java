    private void chooseFile(JTextField textField, final int type) {
        JFileChooser chooser = (lastPath == null ? new JFileChooser() : new JFileChooser(lastPath));
        chooser.setAcceptAllFileFilterUsed(false);
        FileFilter filter;
        if (type == FOLDER) { // folder
            chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            filter = new FileFilter() {

                @Override
                public boolean accept(File f) {
                    return f.isDirectory();
                }

                @Override
                public String getDescription() {
                    return "folder";
                }
            };
        } else { // txt file
            chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
            filter = new FileFilter() {

                public boolean accept(File f) {
                    if (f.isDirectory()) {
                        return true;
                    }
                    String s = f.getName();
                    String ext = null;
                    int i = s.lastIndexOf('.');
                    if (i > 0 && i < s.length() - 1) {
                        ext = s.substring(i + 1).toLowerCase();
                    }
                    return ((type == TXT && "txt".equals(ext)) || (type == JAR && "jar".equals(ext)));
                }

                @Override
                public String getDescription() {
                    if (type == TXT) {
                      return "txt file";
                    } else return "jar archive";
                 }
            };

        }
        chooser.addChoosableFileFilter(filter);
        int returnVal = chooser.showOpenDialog(mainPanel);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            String path = null;
            try {
                path = chooser.getSelectedFile().getCanonicalPath();
                textField.setText(path);
                lastPath = path;
            } catch (IOException ex) {
                Logger.getLogger(ConverterView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }
    
